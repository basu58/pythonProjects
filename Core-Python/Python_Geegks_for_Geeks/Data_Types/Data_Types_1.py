# DataType Output: str
x = "Hello World"
print(type(x))

# DataType Output: int
x = 50
print(type(x))
# DataType Output: float
x = 60.5
print(type(x))
# DataType Output: complex
x = 3j
print(type(x))
# DataType Output: list
x = ["geeks", "for", "geeks"]
print(type(x))
# DataType Output: tuple
x = ("geeks", "for", "geeks")
print(type(x))
# DataType Output: range
x = range(10)
print(type(x))
# DataType Output: dict
x = {"name": "Suraj", "age": 24}
print(type(x))
# DataType Output: set
x = {"geeks", "for", "geeks"}
print(type(x))
# DataType Output: frozenset
x = frozenset({"geeks", "for", "geeks"})
print(type(x))
# DataType Output: bool
x = True
print(type(x))
# DataType Output: bytes
x = b"Geeks"
print(type(x))
# DataType Output: bytearray
x = bytearray(4)
print(type(x))
# DataType Output: memoryview
x = memoryview(bytes(6))
print(type(x))
# DataType Output: NoneType
x = None
print(type(x))