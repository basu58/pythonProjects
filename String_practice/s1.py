# Python Program for
# Creation of String

# Creating a String
# with single Quotes
string1 = 'Welcome to Python world'
print('Welcome to Python world')
print(string1)

# Creating a String
# with double Quotes
string2 = "Welcome to python world"
print("welcome to python world")
print(string2)


# Creating a String
# with triple Quotes
string3 = '''welcome to python "world"'''
print('''welcome to python "world"''')
print(string3)


# Creating String with triple
# Quotes allows multiple lines
string4 = '''
            Welcome
            to
            the
            python
            world'''
print('''
            Welcome
            to
            the
            python
            world''')
print(string4)

